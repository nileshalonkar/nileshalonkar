package com.example.missionk3.model;

public class AudioCatDataModel {
    String id = "", name = "", image = "", type = "", text = "", file_type_status = "", path = "";

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getFile_type_status() {
        return file_type_status;
    }

    public void setFile_type_status(String file_type_status) {
        this.file_type_status = file_type_status;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
